// How to make a peanut butter and jelly sandwich
// Get out all necessary ingredients & supplies: bread, peanut butter, jelly, plate, butter knife, napkins. 
// Take out two slices of bread and place them on the plate.
// Use the knife to spread jelly on the open face of one slice of bread on the plate
// Make sure to cover entire face of bread with a thin layer of jelly. 
// You might want to wipe off knife with napkin to avoid cross contamination
// Use the knife to spread peanut butter on the face of the other slice of bread.
// Make sure to cover entire face of bread with a thin layer of peanut butter
// Carefully take one of the two slices of bread with topping and place it firmly on the other slice of bread, with the topping sides facing each other, aligned. Should look like a neat stack of two bread slices joined by peanut butter and jelly in the middle.
// Cut according to your desires: diagonally, down the middle, you can opt to remove the crusts if you so desire.
// Enjoy!
